/* SQL VIEW statement is a virtual table based on the result-sets. 
In SALES view for getting all information about  -	Invoice number, Invoice date, Customer ID, Customer Name, Customer Surname, Subtotal, VAT charge and Total have been used data from INVOICE and INVOICE_DETAILS tables. */

 CREATE VIEW SALES AS
 SELECT INVOICE.INV_ID AS INVOICE, CUST_ID AS CUSTOMER, CUST_NAME AS NAME, 
		CUST_SURNAME AS SURNAME, SUBTOTAL, VAT_CHARGE AS VAT, TOTAL_PRICE AS PRICE,INVOICE_DETAILS.INV_ID						
 FROM INVOICE, INVOICE_DETAILS 
 WHERE INVOICE.INV_ID = INVOICE_DETAILS.INV_ID 
 GROUP BY INV_ID, CUST_ID, CUST_NAME, CUST_SURNAME, SUBTOTAL, VAT_CHARGE, TOTAL_PRICE
 ORDER BY INV_ID;
 
/* In EMPLOYEE_REPORT view for getting all information about  -	Employee Name,Date,Work order number,Hourly rate,Hours worked and Total Price  have been used data from EMPLOYEE, INVOICE_DETAILS, WORK_ORDER_DETAILS and WORK_ORDER tables. */
 
 CREATE VIEW EMPLOYEE_REPORT AS
 SELECT EMP_NAME AS EMPLOYEENAME, EMP_SURNAME AS EMPLOYEESURNAME, INVOICE_DETAILS.TOTAL_PRICE AS TOTALPRICE, WORKED_HOURS AS WORKEDHOURS, 
		DATE_JOB_STARTED AS JOBSTARTED, DATE_JOB_COMPLETED AS JOBFINISHED, WORK_ORDER.WORK_ORDER_ID AS WORKORDERID, HOURLY_RATE AS HOURLYRATE
 FROM EMPLOYEE, INVOICE_DETAILS, WORK_ORDER_DETAILS, WORK_ORDER 
 GROUP BY EMP_NAME, EMP_SURNAME, INVOICE_DETAILS.TOTAL_PRICE, WORKED_HOURS, DATE_JOB_STARTED, DATE_JOB_COMPLETED, WORK_ORDER.WORK_ORDER_ID, HOURLY_RATE
 ORDER BY WORK_ORDER.WORK_ORDER_ID;
 
 

  
 


 
 